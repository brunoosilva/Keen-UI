<script>
  import { Storage } from 'secure-providers';

  export default {
    name: 'sc-chart-map-svg',
    props: {
      data: {
        type: Object,
        required: true,
      },
    },
    computed: {
      heatmap() {
        return this.colour_heat_map(this.data);
      },
    },
    methods: {
      colour_heat_map(shipmentAmount) {
        const heatmap = {};
        let max = 0.0001;

        for (const id in shipmentAmount) {
          max = Math.max(shipmentAmount[id].amount, max);
        }

        for (const id in shipmentAmount) {
          const mathSin = Math.sin((shipmentAmount[id].amount / max) * Math.PI * 0.5);

          // Hue
          let hue = mathSin * 59.0;
          hue = Math.round(129 - hue);

          // Saturation
          let saturation = mathSin * 59.0;
          saturation = Math.round(49 - saturation);
          saturation = `${saturation}%`;

          // Lightness
          let lightness = mathSin * 89.0;
          lightness = (lightness * 10.0) * 0.1;
          lightness = Math.round(81 - lightness);
          lightness = `${lightness}%`;

          const hsl = `hsl(${hue}, ${saturation}, ${lightness})`;

          heatmap[id.toLowerCase()] = hsl;
        }

        return heatmap;
      },
      getUf(event) {
        this.$emit('currentState', event.target.id.toUpperCase());
      },
      leavingUf() {
        this.$emit('leavingUf');
      },
      filter_transactions(uf) {
        const type = 'region';
        const view_type = Storage.get('viewType') || 'user';
        window.location.href = `/ng#/transactions/filter/?type=${type}&uf=${uf}&view_type=${view_type}`;
      },
    },
  };
</script>

